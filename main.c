#include "get_next_line.h"
#include <fcntl.h>
#include <stdio.h>

int	main(void)
{
	int		fd;
	char	*line;

	fd = open("test.txt", O_RDONLY);
	line = get_next_line(fd);
	while (line && line[0] != '\0')
	{
		printf("%s", line);
		line = get_next_line(fd);
	}
	return (0);
}
